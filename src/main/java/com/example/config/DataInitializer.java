    @Bean
    public CommandLineRunner initDatabase(JdbcTemplate jdbcTemplate) {
        return args -> {
            try {
                // 先删旧表
                jdbcTemplate.execute("DROP TABLE IF EXISTS grade CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS course CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS student CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS teacher CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS classroom CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS notice CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS chat_message CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS chat_session CASCADE");
                jdbcTemplate.execute("DROP TABLE IF EXISTS users CASCADE");

                // ====== 教室表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS classroom (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        code VARCHAR(20) NOT NULL UNIQUE,
                        building VARCHAR(50),
                        room_no VARCHAR(10),
                        capacity INT,
                        has_ac BOOLEAN DEFAULT true,
                        has_multimedia BOOLEAN DEFAULT true,
                        notes VARCHAR(200)
                    )
                    """);

                // ====== 教师表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS teacher (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        teacher_no VARCHAR(20) NOT NULL UNIQUE,
                        name VARCHAR(50) NOT NULL,
                        password VARCHAR(100) NOT NULL DEFAULT '123456',
                        enrollment_year INT,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                    """);

                // ====== 学生表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS student (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        student_no VARCHAR(20) NOT NULL UNIQUE,
                        name VARCHAR(50) NOT NULL,
                        password VARCHAR(100) NOT NULL DEFAULT '123456',
                        major VARCHAR(100),
                        class_name VARCHAR(50),
                        enrollment_year INT
                    )
                    """);

                // ====== 课程表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS course (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        course_code VARCHAR(20),
                        course_name VARCHAR(100) NOT NULL,
                        major VARCHAR(100),
                        semester VARCHAR(20),
                        credits DOUBLE PRECISION,
                        teacher_id BIGINT,
                        schedule_day VARCHAR(10),
                        schedule_time VARCHAR(30),
                        start_time VARCHAR(20),
                        classroom VARCHAR(50)
                    )
                    """);

                // ====== 成绩表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS grade (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        student_id BIGINT NOT NULL,
                        course_id BIGINT NOT NULL,
                        score DOUBLE PRECISION,
                        grade_point DOUBLE PRECISION,
                        semester VARCHAR(20)
                    )
                    """);

                // ====== 公告/聊天表 ======
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS notice (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        title VARCHAR(255) NOT NULL, content TEXT NOT NULL,
                        publish_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                    """);
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS chat_session (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id BIGINT NOT NULL, title VARCHAR(255) NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                    """);
                jdbcTemplate.execute("""
                    CREATE TABLE IF NOT EXISTS chat_message (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        session_id BIGINT NOT NULL, role VARCHAR(20) NOT NULL,
                        content TEXT NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                    """);

                // 清空向量库脏数据
                try { jdbcTemplate.update("DELETE FROM vector_store"); } catch (Exception ignored) {}

                seedAll(jdbcTemplate);

            } catch (Exception e) {
                log.error("数据库初始化失败", e);
            }
        };
    }

