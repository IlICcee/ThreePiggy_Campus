    private void insertCourse(JdbcTemplate j, String code, String name, String major,
                              double credits, String teacherNo, String day, String slot, String time, String room) {
        Long tid = j.queryForObject("SELECT id FROM teacher WHERE teacher_no = ?", Long.class, teacherNo);
        j.update("INSERT INTO course (course_code, course_name, major, credits, teacher_id, schedule_day, schedule_time, start_time, classroom, semester) VALUES (?,?,?,?,?,?,?,?,?,?)",
                code, name, major, credits, tid, day, slot, time, room, SEM);
    }

    private void grade(JdbcTemplate j, String studentNo, String courseCode, double score) {
        double gp = score >= 90 ? 4.0 : score >= 85 ? 3.7 : score >= 82 ? 3.3 :
                    score >= 78 ? 3.0 : score >= 75 ? 2.7 : score >= 72 ? 2.3 :
                    score >= 68 ? 2.0 : score >= 64 ? 1.5 : score >= 60 ? 1.0 : 0.0;
        j.update("INSERT INTO grade (student_id, course_id, score, grade_point, semester) " +
                "SELECT s.id, c.id, ?, ?, ? FROM student s, course c " +
                "WHERE s.student_no = ? AND c.course_code = ?", score, gp, SEM, studentNo, courseCode);
    }
